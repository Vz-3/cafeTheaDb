    private void PlaceOrderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PlaceOrderButtonActionPerformed
        PlaceOrder newOrder = new PlaceOrder();
        newOrder.setVisible(true);
        
        DefaultTableModel model = (DefaultTableModel) orderTable.getModel();
        
        ArrayList<Integer> numData = new ArrayList<>();
        for (int count = 0; count < model.getRowCount(); count++){
            System.out.println(Integer.valueOf(model.getValueAt(count, 1).toString())*Integer.valueOf(model.getValueAt(count, 2).toString()));
            numData.add(
                    Integer.valueOf(model.getValueAt(count, 1).toString())*Integer.valueOf(model.getValueAt(count, 2).toString())
            );
        }
        int tempSum = 0;
        for(int value: numData){
            tempSum += value;
        }
        newOrder.changeTextValue(Integer.toString(tempSum));
    }//GEN-LAST:event_PlaceOrderButtonActionPerformed

